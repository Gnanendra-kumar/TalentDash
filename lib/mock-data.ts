export function getCompanyBySlug(slug: string): Company | undefined {
  return companies.find((c) => c.slug === slug);
}

export function getSalariesByCompany(companyId: string): Salary[] {
  return salaries.filter((s) => s.company_id === companyId);
}

export function getSalaryById(id: string): Salary | undefined {
  return salaries.find((s) => s.id === id);
}

export function getAllUniqueRoles(): string[] {
  return [...new Set(salaries.map((s) => s.role))].sort();
}

export function getAllUniqueLocations(): string[] {
  return [...new Set(salaries.map((s) => s.location))].sort();
}
